/**
 * Comprehensive Exercise Database
 * 
 * Contains detailed information for each exercise including:
 * - ID: Unique identifier
 * - Name: Exercise name
 * - Movement pattern: Type of movement
 * - Primary Muscle Group: Main muscle targeted
 * - Secondary Muscle Groups: Additional muscles worked
 * - Equipment Required: Equipment needed
 * - Difficulty Level: Beginner, intermediate, or advanced
 * - Potential Risks/Injuries: Common risks associated with the exercise
 * - Alternative movements: Similar exercises
 */

export type MovementPattern = 
  | 'push' 
  | 'pull' 
  | 'squat' 
  | 'hinge' 
  | 'lunge' 
  | 'carry' 
  | 'rotation' 
  | 'gait' 
  | 'isometric' 
  | 'stretch' 
  | 'cardio';

export type Difficulty = 'beginner' | 'intermediate' | 'advanced';

export type Equipment = 
  | 'bodyweight' 
  | 'dumbbells' 
  | 'barbell' 
  | 'kettlebell' 
  | 'resistance bands' 
  | 'cable machine' 
  | 'smith machine' 
  | 'landmine' 
  | 'pull-up bar' 
  | 'bench' 
  | 'mat' 
  | 'none';

export type MuscleRegion = 
  | 'upper'    // Upper portion of muscle (e.g., upper chest, upper back)
  | 'mid'      // Middle portion of muscle (e.g., mid chest, mid back)
  | 'lower'    // Lower portion of muscle (e.g., lower chest, lower back)
  | 'inner'    // Inner portion (e.g., inner biceps, inner quads)
  | 'outer'    // Outer portion (e.g., outer biceps, outer quads)
  | 'front'    // Front portion
  | 'rear'     // Rear portion
  | 'lateral'  // Side portion
  | 'medial'   // Medial portion
  | 'full';    // Targets entire muscle group

export interface ExerciseData {
  id: string;
  name: string;
  movementPattern: MovementPattern;
  primaryMuscleGroup: string;
  secondaryMuscleGroups: string[];
  equipmentRequired: Equipment[];
  difficulty: Difficulty;
  potentialRisks: string[];
  alternatives: string[];
  category: 'strength' | 'cardio' | 'flexibility' | 'balance' | 'stability';
  // Muscle region targeting - specifies which part of the primary muscle group is targeted
  muscleRegion?: MuscleRegion;
  // Legacy support - keep muscleGroups for backward compatibility
  muscleGroups?: string[];
  equipment?: Equipment[];
  // Video demonstration URL - can be local file path, cloud URL, or YouTube/Vimeo link
  videoUrl?: string;
  // Video thumbnail URL (optional)
  videoThumbnail?: string;
}

