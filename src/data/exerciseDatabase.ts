/**
 * Get exercise data by name
 */
export const getExerciseData = (name: string): ExerciseData | undefined => {
  return exerciseDatabase.find(ex => ex.name.toLowerCase() === name.toLowerCase());
};

/**
 * Get exercise data by ID
 */
export const getExerciseById = (id: string): ExerciseData | undefined => {
  return exerciseDatabase.find(ex => ex.id === id);
};

/**
 * Get exercises by category
 */
export const getExercisesByCategory = (category: 'strength' | 'cardio' | 'flexibility' | 'balance' | 'stability'): ExerciseData[] => {
  return exerciseDatabase.filter(ex => ex.category === category);
};

/**
 * Get exercises by equipment
 */
export const getExercisesByEquipment = (equipment: Equipment): ExerciseData[] => {
  return exerciseDatabase.filter(ex => ex.equipmentRequired.includes(equipment));
};

/**
 * Get exercises by difficulty
 */
export const getExercisesByDifficulty = (difficulty: Difficulty): ExerciseData[] => {
  return exerciseDatabase.filter(ex => ex.difficulty === difficulty);
};

/**
 * Get exercises by movement pattern
 */
export const getExercisesByMovementPattern = (pattern: MovementPattern): ExerciseData[] => {
  return exerciseDatabase.filter(ex => ex.movementPattern === pattern);
};

/**
 * Get alternative exercises for a given exercise
 */
export const getAlternativeExercises = (exerciseName: string): ExerciseData[] => {
  const exercise = getExerciseData(exerciseName);
  if (!exercise) return [];
  
  return exercise.alternatives
    .map(altName => getExerciseData(altName))
    .filter((ex): ex is ExerciseData => ex !== undefined);
};
